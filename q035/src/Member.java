
public class Member {
	
	private String name;
	private String tel;
	private String sex;
	
	public Member(String n, String t, String s) {
		this.name = n;
		this.tel = t;
		this.sex = s;
	}
	
	public String getName() {
		
		return name;
	}
	public String getTel() {
		
		return tel;
	}
	public String getSex() {
		
		return sex;
	}
	
}
